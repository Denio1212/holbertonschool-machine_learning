#!/usr/bin/env python3
"""
Returns a new list that contains both arrays concatenated together
"""


def cat_arrays(arr1, arr2):
    """
    Returns a new list that contains both arrays concatenated
    Args:
        arr1: First array
        arr2: Second array

    Returns: A new list with both arrays concatenated
    """
    concatenated = arr1 + arr2
    return concatenated
